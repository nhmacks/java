public class _07OperadoresAsignacion {
    public static void main(String arg[]) {
        int a = 3, b = 2;
        int c = a + 5 - b;
        a += 1; // a = a + 1;
        System.out.println(a);
        a += 3; // a = a + 3;
        System.out.println(a);
        a -= 2; // a = a + 2;
        System.out.println(a);
        System.out.println(c);
        // *=   /=    %=
    }
}
